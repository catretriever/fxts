# 200 bars ending at 2026-03-04 15:00 UTC  (starts 2026-02-24 08:00)
_HOURLY_END   = datetime.datetime(2026, 3, 4, 15, 0, 0)
_HOURLY_START = _HOURLY_END - datetime.timedelta(hours=199)


def _gen_bars(ticker, base_open, step, hl_frac, start_id, n=200):
    """Return n consecutive hourly OHLC rows as lists."""
    rows = []
    o = base_open
    for i in range(n):
        dt = _HOURLY_START + datetime.timedelta(hours=i)
        c = round(o + step, 6)
        h = round(c + hl_frac, 6)
        lo = round(o - hl_frac, 6)
        rows.append([
            start_id + i, ticker,
            dt.strftime('%Y-%m-%d'), dt.strftime('%H:%M:%S'),
            f'{o:.5f}', f'{h:.5f}', f'{lo:.5f}', f'{c:.5f}',
        ])
        o = c
    return rows


def _all_hourly():
    # (ticker, base_open, step_per_bar, high_low_half_range)
    # Steps chosen so final price matches prior sample data closely.
    specs = [
        ('GBPJPY', 185.000, 0.03284,  0.150),   # →191.54
        ('EURUSD',  1.0620, 0.000130, 0.00060),  # →1.0879
        ('GBPUSD',  1.2450, 0.000120, 0.00060),  # →1.2689
        ('USDJPY', 146.000, 0.02191,  0.100),    # →150.36
        ('USDCAD',  1.3320, 0.000170, 0.00075),  # →1.3659
        ('EURGBP',  0.8420, 0.000090, 0.00035),  # →0.8599
        ('EURJPY', 155.000, 0.04296,  0.150),    # →163.55
        ('EURCAD',  1.4120, 0.000250, 0.00075),  # →1.4618
    ]
    rows = []
    sid = 1
    for ticker, base_open, step, hl in specs:
        rows.extend(_gen_bars(ticker, base_open, step, hl, start_id=sid))
        sid += 200
    return rows


