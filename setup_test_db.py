_ENGINES_CROSSES = [
    ('GBPJPYeng01', 'GBPJPY'),
    ('EURUSDeng01', 'EURUSD'),
    ('GBPUSDeng01', 'GBPUSD'),
    ('USDJPYeng01', 'USDJPY'),
    ('USDCADeng01', 'USDCAD'),
    ('EURGBPeng01', 'EURGBP'),
    ('EURJPYeng01', 'EURJPY'),
    ('EURCADeng01', 'EURCAD'),
]

_MA_PAIRS = [
    ( 6,   7),   # tight — existing
    (12,  13),   # tight — existing
    ( 6,  13),   # short-term momentum
    ( 8,  21),   # Fibonacci intraday
    (20,  50),   # swing
    (50, 200),   # Golden/Death Cross
]


def _build_siggen_rows():
    """Return (siggentp_rows, map_rows) for all engines × all MA pairs."""
    sg_rows, map_rows = [], []
    sg_id, map_id = 1, 1
    for eng, cross in _ENGINES_CROSSES:
        for fast, slow in _MA_PAIRS:
            name = f'{cross}_TP_{fast}_{slow}'
            sg_rows.append([sg_id, name, fast, slow])
            map_rows.append([map_id, eng, name])
            sg_id  += 1
            map_id += 1
    return sg_rows, map_rows


_SIGGENTP_ROWS, _MAP_TE_SG_ROWS = _build_siggen_rows()

